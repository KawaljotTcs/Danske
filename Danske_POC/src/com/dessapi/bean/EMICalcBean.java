package com.dessapi.bean;

public class EMICalcBean {
	private double principal;
	private double rate_of_interest;
	private double number_of_months;
	private double emi;
	private double total_interest;
	private double total_amount;
	public double getPrincipal() {
		return principal;
	}
	public void setPrincipal(double principal) {
		this.principal = principal;
	}
	public double getRate_of_interest() {
		return rate_of_interest;
	}
	public void setRate_of_interest(double rate_of_interest) {
		this.rate_of_interest = rate_of_interest;
	}
	public double getNumber_of_months() {
		return number_of_months;
	}
	public void setNumber_of_months(double number_of_months) {
		this.number_of_months = number_of_months;
	}
	public double getEmi() {
		return emi;
	}
	public void setEmi(double emi) {
		this.emi = emi;
	}
	public double getTotal_interest() {
		return total_interest;
	}
	public void setTotal_interest(double total_interest) {
		this.total_interest = total_interest;
	}
	public double getTotal_amount() {
		return total_amount;
	}
	public void setTotal_amount(double total_amount) {
		this.total_amount = total_amount;
	}
	
	
	
	
	

}
